package functions

//GreaterThan |
func GreaterThan(input float64, value float64) bool {
	return input > value
}

//GreaterThanOrEqual |
func GreaterThanOrEqual(input float64, value float64) bool {
	return input >= value
}

//LessThan |
func LessThan(input float64, value float64) bool {
	return input < value
}

//LessThanOrEqual |
func LessThanOrEqual(input float64, value float64) bool {
	return input <= value
}
